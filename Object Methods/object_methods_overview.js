/* Object Methods 

Object methods in JavaScript allow objects to perform actions and manipulate their own data. 

Commonly used Object methods are:

1. Object.keys(obj): Returns an array of the object's property names (keys).

2. Object.values(obj): Returns an array of the object's property values.

3. Object.entries(obj): Returns an array of [key, value] pairs for each property in the object.

4. Object.assign(target, ...sources): Copies properties from one or more source objects to a target object.

5. Object.freeze(obj): Prevents any modifications to an object, making it immutable.

6. Object.seal(obj): Prevents adding or removing properties but allows modification of existing properties.

*/