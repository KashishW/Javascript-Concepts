/* Destructuring in Javascript

- Destructuring is a way to assign elements of arrays and objects into individual variables for quick access.

- Types of Destructuring in Javascript

    - Array Destructuring
    - Object Destructuring

*/

/* Object Destructuring

- Object destructuring allows you to extract properties from an object and assign them to variables  for quick access.

*/


const {name, age} ={
    name : 'Kashish',
    age: 30
}

console.log(name);
console.log(age);