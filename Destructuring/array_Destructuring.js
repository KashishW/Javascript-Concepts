/* Destructuring in Javascript

- Destructuring is a way to assign elements of arrays and objects into individual variables for quick access.

- Types of Destructuring in Javascript

    - Array Destructuring
    - Object Destructuring

*/

// Array Destructuring: Array destructuring allows you to extract values from an array and assign them to variables for quick access.
// Syntax []

const [a,b] = [1,2]

console.log(a);  //1
console.log(b);  //2





